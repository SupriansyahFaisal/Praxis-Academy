public class Variable {
    public static void main(String[] args) {
        // Variable tidak boleh mengandung whiteSpace
        String name;
        name = "Faisal supriansyah";

        int age = 30;
        String address = "Umbulharjo";

        System.out.println(name + " " + age + " " + address);

        // Variable bisa menggunakan var (harus menginput value secara langsung)

        var firstname = "Ryan";
        var lastname = "Ardiansyah";
        System.out.println(firstname + lastname);
    }
}
